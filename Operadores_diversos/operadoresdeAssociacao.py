#Operadores de associação

# O computador, o programa, lê caractere por caractere
# Primeiro lê o 'm', depois 'a','r'..
# Um caractere é uma letra por exemplo, ou @, # ..
nome = 'Marcos'

# Operador in      - x in y
# Operador not in  - x not in y

x = 'pernambuco'

y = 'per'

print(y in x)
# Usando in, queremos saber se a variável y está em x
# E o valor retornado é True
# Pois 'per' está em 'pernambuco'

a = 'agua'
b = 'fogo'

print(a in b)
# O resultado deu False, pois água não está em fogo

print(a not in b)
# O resultado deu True, pois água não está em fogo




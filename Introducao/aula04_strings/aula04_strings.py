# Strings

# Podemos usar aspas simples ao definir ou imprimir uma string
# print('Ana')
# print ("Ana")

# Podemos atribuir um comentário (que tbm é uma String) a uma variável e quebra-lá usando um espaço de linha
a ="""
Olá,
este é o curso de Python da Danki Code!
"""
print(a)
# A vantagem é que podemos criar um texto em Bloco usando comentários


# Métodos das Strings:
# Dando espaços no começo depois das aspas
b = " Olá Mundo! "
print(b)

# Usando o strip
b = " Olá Mundo!"
print(b.strip())
# O strip irá ignorar os espaçamentos

# Usando o upper
b = " Olá Mundo!"
print(b.upper())
# O upper para deixar as letras em maiúsculas

# Usando o lower
b = " Olá Mundo!"
print(b.lower())
# O lower para deixar as letras em minúsculos




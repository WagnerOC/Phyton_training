# Tipos de dados

#   a = "Olá Mundo"      String   str
#   b = 5                Number   int 
#   c = 3.1              Float    float
#   d = 1+2j             Complex  complex

# Para saber o tipo de dado, usamos type()

a = 'Olá Mundo'
b = 5
c = 3.1
d = 1+2j
print(type(a))
print(type(b))
print(type(c))
print(type(d))



